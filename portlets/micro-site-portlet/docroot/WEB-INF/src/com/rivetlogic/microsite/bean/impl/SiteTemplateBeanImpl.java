package com.rivetlogic.microsite.bean.impl;

import com.rivetlogic.microsite.bean.SiteTemplateBean;

public class SiteTemplateBeanImpl implements SiteTemplateBean {
    protected long id;
    protected String name;

    public SiteTemplateBeanImpl() {
        
    }
    
    public SiteTemplateBeanImpl(String name) {
        this.name = name;
    }
    
    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
}
